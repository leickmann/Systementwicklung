package Aufgabe8;

public class Beleuchtung {
	public void ein() {
		System.out.println("Beleuchtung eingeschaltet");
	}
	
	public void aus() {
		System.out.println("Beleuchtung ausgeschaltet");
	}
	
	public void dimmen(int wert) {
		System.out.println("Beleuchtung auf Wert gedimmt: " + wert);
	}
}
