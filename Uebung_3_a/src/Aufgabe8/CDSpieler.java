package Aufgabe8;

public class CDSpieler {
	public void ein() {
		System.out.println("CD-Spieler eingeschaltet");
	}
	
	public void aus() {
		System.out.println("CD-Spieler ausgeschaltet");
	}
	
	public void auswerfen() {
		System.out.println("CD-Spieler: CD ausgeworfen");
	}
	
	public void pause() {
		System.out.println("CD-Spieler: pause");
	}
	
	public void spielen() {
		System.out.println("CD-Spieler: play");
	}
	
	public void anhalten() {
		System.out.println("CD-Spieler: angehaltens");
	}
}
