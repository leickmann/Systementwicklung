package Aufgabe8;

public class DVDSpieler {
	public void ein() {
		System.out.println("DVD-Spieler eingeschaltet");
	}
	
	public void aus() {
		System.out.println("DVD-Spieler ausgeschaltet");
	}
	
	public void auswerfen() {
		System.out.println("DVD-Spieler: DVD ausgeworfen");
	}
	
	public void pause() {
		System.out.println("DVD-Spieler: pause");
	}
	
	public void spielen() {
		System.out.println("DVD-Spieler: play");
	}
	
	public void anhalten() {
		System.out.println("DVD-Spieler: angehaltens");
	}
	
	public void setSurroundSound() {
		System.out.println("DVD-Spieler auf Surround-Sound gesetz");
	}
	
	public void setZweiKanalTon() {
		System.out.println("DVD-Spieler auf Zweikanal-Sound gesetzt");
	}
	
	

}
